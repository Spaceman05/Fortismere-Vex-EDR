#pragma config(Motor,  port2,           rightMotor2,    tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port3,           leftMotor,    tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           leftMotor2,   tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           middleMotor,   tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port6,           leftArmMotor,  tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port7,           rightArmMotor, tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port8,           clawMotor,    tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port9,           clawMotor2,    tmotorVex393_MC29, openLoop)
//*!!Code automatically generated by 'ROBOTC' configuration wizard               !!*//

// global config
short armUpSpeed = 127;
short armDownSpeed = -127;
short clawSpeed = 64;
bool firstRun = true;

void autonomous01()
{
	motor[leftArmMotor] = armUpSpeed;
	motor[rightArmMotor] = armUpSpeed;

	motor[leftMotor] = 100;
	motor[leftMotor2] = 100;

	motor[rightMotor] = 100;
	motor[rightMotor2] = 100;

	motor[clawMotor] = clawSpeed;
	motor[clawMotor2] = -clawSpeed;

	sleep(1000);

	motor[clawMotor] = 0;
	motor[clawMotor2] = 0;

	sleep(500);

	motor[leftArmMotor] = 0;
	motor[rightArmMotor] = 0;

	sleep(2000);

	motor[leftMotor] = 0;
	motor[leftMotor2] = 0;

	motor[rightMotor] = 0;
	motor[rightMotor2] = 0;
}

task main()
{
	while (true)
	{
		while (firstRun == true)
		{
			if(vexRT[Btn7U] == 1)
			{
				autonomous01();
				firstRun = false;
			}
			else if(vexRT[Btn7D] == 1)
			{
				firstRun = false;
			}
		}
		motor[port1] = vexRT[Ch1];
		motor[port2] = vexRT[Ch1];

		motor[leftMotor] = vexRT[Ch2];
		motor[leftMotor2] = vexRT[Ch2];

		motor[rightMotor] = vexRT[Ch3];
		motor[rightMotor2] = vexRT[Ch3];

		motor[middleMotor] = (vexRT[Ch4] + vexRT[Ch1])/2;

		if(vexRT[Btn5D] == 1)
		{
			motor[leftArmMotor] = armDownSpeed;
			motor[rightArmMotor] = armDownSpeed;
		}
		else if(vexRT[Btn5U] == 1)
		{
			motor[leftArmMotor] = armUpSpeed;
			motor[rightArmMotor] = armUpSpeed;
		}
		else
		{
			motor[leftArmMotor] = 0;
			motor[rightArmMotor] = 0;
		}

		if(vexRT[Btn6U])
		{
			motor[clawMotor] = clawSpeed;
			motor[clawMotor2] = -clawSpeed;
		}
		else if(vexRT[Btn6D])
		{
			motor[clawMotor2] = clawSpeed;
			motor[clawMotor] = -clawSpeed;
		}
		else if(vexRT[Btn8U])
		{
			motor[clawMotor] = clawSpeed;
		}
		else if(vexRT[Btn8D])
		{
			motor[clawMotor] = -clawSpeed;
		}
		else if(vexRT[Btn8L])
		{
			motor[clawMotor2] = clawSpeed;
		}
		else if(vexRT[Btn8R])
		{
			motor[clawMotor2] = -clawSpeed;
		}
		else
		{
			motor[clawMotor] = 0;
			motor[clawMotor2] = 0;
		}
	}
}
